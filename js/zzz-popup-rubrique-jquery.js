$(document).ready(function() {
 $( ".button-pop-art" ).click(function() {
      
       $('#pop-art').css("visibility", "visible"); 
       $('#pop-art').css("opacity", 1);
       });
  
  $( ".close-pop-art" ).click(function() {

       $('#pop-art').css("visibility", "hidden"); 
       $('#pop-art').css("opacity", 0);
    });

 $( ".button-pop-list" ).click(function() {
      
       $('#pop-art-list').css("visibility", "visible"); 
       $('#pop-art-list').css("opacity", 1);
       });
  
  $( ".close-pop-list" ).click(function() {

       $('#pop-art-list').css("visibility", "hidden"); 
       $('#pop-art-list').css("opacity", 0);
    });

 $( ".button-pop-art-slider" ).click(function() {
      
       $('#pop-art-slider').css("visibility", "visible"); 
       $('#pop-art-slider').css("opacity", 1);
       });
  
  $( ".close-pop-art-slider" ).click(function() {

       $('#pop-art-slider').css("visibility", "hidden"); 
       $('#pop-art-slider').css("opacity", 0);
    });
});